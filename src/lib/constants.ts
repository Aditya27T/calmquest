export const calculatePSSScore = (answers: Record<number, number>) => {
  const totalScore = Object.values(answers).reduce((acc, curr) => acc + curr, 0);
  // PSS dengan 10 pertanyaan, skor maksimal: 40 (10 pertanyaan × 4 poin)
  if (totalScore <= 13) return { level: "Rendah", message: "Tingkat stres Anda rendah", score: totalScore };
  if (totalScore <= 26) return { level: "Sedang", message: "Tingkat stres Anda sedang", score: totalScore };
  return { level: "Tinggi", message: "Tingkat stres Anda tinggi", score: totalScore };
};

export const calculateBDIScore = (answers: Record<number, number>) => {
  const totalScore = Object.values(answers).reduce((acc, curr) => acc + curr, 0);
  // BDI dengan 10 pertanyaan, skor maksimal: 30 (10 pertanyaan × 3 poin)
  if (totalScore <= 9) return { level: "Normal", message: "Tidak ada indikasi depresi", score: totalScore };
  if (totalScore <= 15) return { level: "Ringan", message: "Indikasi depresi ringan", score: totalScore };
  if (totalScore <= 20) return { level: "Sedang", message: "Indikasi depresi sedang", score: totalScore };
  if (totalScore <= 25) return { level: "Cukup Berat", message: "Indikasi depresi cukup berat", score: totalScore };
  return { level: "Berat", message: "Indikasi depresi berat", score: totalScore };
};